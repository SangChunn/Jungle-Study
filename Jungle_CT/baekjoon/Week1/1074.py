from sys import stdin

N, r, c = map(int,(stdin.readline().split()))
#[0][0] -> [0][1] -> [1][0] -> [1][1]  N=1 2^N-1
#[0][0] -> [0][1] -> [1][0] -> [1][1]  -> [0][2] -> [0][3] -> [1][2] -> [1][3] 
#-> [2][0] -> [2][1] -> [3][0]->[3][1] -> [2][2] -> [2][3] -> [3][2] -> [3][3]. N=2
# 2*r%2 + c%2 + 4 + 4rc(N-1, r//2 c//2 )
def RC(N, r, c):
    if N == 0:
        return 0
    else:
        return (2 * (r % 2) + c % 2 + 4*RC(N-1, r//2, c//2))
print(RC(N, r, c)) 